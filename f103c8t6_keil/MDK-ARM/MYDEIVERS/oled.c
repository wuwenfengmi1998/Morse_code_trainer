void OLED_Ascii(unsigned char x,unsigned char y,unsigned char size,char chr,unsigned char type)
{
	if(chr>=' '&&chr<='~')
	{
		switch(size)
		{
			case 8:
				OLED_Pix(x,y,5,8,asc2_0805[chr-' '],type);
				break;			
			case 12:
				OLED_Pix(x,y,6,12,asc2_1206[chr-' '],type);
				break;
			case 16:
				OLED_Pix(x,y,8,16,asc2_1608[chr-' '],type);
				break;
		}	
	}

}

void OLED_Str(unsigned char x,unsigned char y,unsigned char size,char *str,unsigned char type)
{
	unsigned char size2;
	switch(size)
	{
		case 8:
			size2=6;
		break;
		case 12:
			size2=7;
		break;
		case 16:
			size2=8;
		break;
	}	
	
	while(*str!='\0')
	{
		if(x+size2>=X_WIDTH)
		{
			y+=size;
			if(y>=Y_WIDTH)
			{
				y=0;
			}
			x=0;
		}

		OLED_Ascii(x,y,size,*str,type);

		str++;
		
		x+=size2;
		
		

	}

}

