/*
将显存发送到硬件
*/
void OLED_Cache_to_hardware()
{
	for(uint8_t y=0;y<Y_WIDTH_;y++)
	{
		OLED_Set_Pos(0,y);
		for(uint8_t x=0;x<X_WIDTH;x++)
		{
			OLED_WrDat(OLED_buff[y][x]);
		}
	}
}


