/*
打点函数
打点类型
dot_type=0 清除点
				=1 打点
				=2 取反点
*/
void OLED_set_dot(unsigned char x,unsigned char y,unsigned char dot_type)
{
	if(x>=X_WIDTH||y>=Y_WIDTH)
	{
		return ;
	}
	uint8_t y1=y/8;
	switch(dot_type)
	{
		case 0:
			OLED_buff[y1][x] &= ~(1<<(y%8));
			break;
		case 1:
			OLED_buff[y1][x] |= (1<<(y%8));
			break;
		case 2:
			OLED_buff[y1][x] ^= (1<<(y%8));
			break;
		case 3:break;
	}
	
}
